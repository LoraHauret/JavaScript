function getStatistic(text) {
  if (typeof text != "string") throw new Error("–∞—Ä–≥—É–º–µ–Ω—Ç –º–µ—Ç–æ–¥–∞ - –Ω–µ —Å—Ç—Ä–æ–∫–∞");
  let letters = 0;
  let digits = 0;
  let punctuation = 0;
  let other = 0;
  for (let ch of text) {
    if (/[a-z–∞-—è—ë]/i.test(ch)) letters++;
    else if (/\d/.test(ch)) digits++;
    else if (/[.,?!@#$%^&*()_\-\[\]{};:]/.test(ch)) punctuation++;
    else other++;
  }
  return { letters, digits, punctuation, other };
}

